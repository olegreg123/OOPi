public class Home {
    private String name;
    private double age;
    private String color;

    public Home(String name, double age, String color) {
        this.age = age;
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public double getAge() {
        return age;
    }

    public String getColor() {
        return color;
    }

}

